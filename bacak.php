Halterin yer aldığı barı ya da squat barını omuzlarına göre şekillendirin.
Barın vücudunuzda sabit kalmasına özen gösterin.
Sabit kalması için ellerinizi ve omuzlarınızı kullanın.
Ayaklarınızı omuz hizasında açın.
